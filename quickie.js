let left_operand = 6;
let right_operand = 4;
let operator = "+";
	

	if (operator = "+"){
	console.log(left_operand +right_operand);
}else if (operator = "-"){
	console.log(left_operand -right_operand);
}else if (operator = "*"){
	console.log(left_operand * right_operand);
}else if (operator = "/"){
	console.log(left_operand +right_operand);
}else {
	console.log("NaN");
}



